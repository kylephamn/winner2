class CustomVideoDataset(Dataset):
    def __init__(self, annotations_file, video_dir):
        # Build a flat list of (video_path, start_frame, end_frame, label)
        self.samples = []
        for csv_path, video_path in zip(annotations_file, video_dir):
            df = pd.read_csv(csv_path, skiprows=2)
            df.columns = ["compression_num", "start_frame", "end_frame"]
            compression_ranges = list(zip(df["start_frame"], df["end_frame"]))

            # Get total frame count to sample non-compression clips
            cap = cv.VideoCapture(video_path)
            total_frames = int(cap.get(cv.CAP_PROP_FRAME_COUNT))
            cap.release()

            # Positive samples (compression)
            for start, end in compression_ranges:
                self.samples.append((video_path, start, end, 1))

            # Negative samples (non-compression) — gaps between compressions
            prev_end = 0
            for start, end in compression_ranges:
                if start - prev_end > 10:  # ignore tiny gaps
                    self.samples.append((video_path, prev_end, start, 0))
                prev_end = end
            if total_frames - prev_end > 10:
                self.samples.append((video_path, prev_end, total_frames, 0))

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        video_path, start, end, label = self.samples[idx]
        frames = video_to_PIL(video_path, start, end)
        return frames, label

