def video_to_PIL(video, start_frame=0, end_frame=None, max_frames=64):
    frames = []
    cap = cv.VideoCapture(video)
    cap.set(cv.CAP_PROP_POS_FRAMES, start_frame)

    frame_idx = start_frame
    while True:
        ret, frame = cap.read()
        if not ret or (end_frame and frame_idx >= end_frame):
            break
        frames.append(Image.fromarray(cv.cvtColor(frame, cv.COLOR_BGR2RGB)))
        frame_idx += 1
        if len(frames) >= max_frames:
            break

    cap.release()
    return frames

